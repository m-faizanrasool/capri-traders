     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // return  $request;
        $request->validate([
            "action"=> 'required',
            "company_head_id"=> 'required',
            "type"=> 'required',
            "party_id"=> 'required',
            "date"=> 'required',
            "amount"=> 'required',
        ]);

        $payment = Payment::create(request([
            "company_head_id",
            "party_id",
            "sale_ids",
            "action",
            "type",
            "date",
            "cheque_no",
            "cheque_date",
            "narration",
            "remarks",
            "tax",
            "amount",
        ]));

        return response()->json(["message" => 'Saved Successfully!']);
    }

